dados = {"nome": "Paulo", "idade": 28, "telefone": "3333-1234"}

print(dados["nome"]) # Paulo
print(dados["idade"]) # 28
print(dados["telefone"]) # 3333-1234

dados["nome"] = "Vanessa"
dados["idade"] = 29
dados["telefone"] = "1234-3333"

print(dados) # {'nome': 'Vanessa', 'idade': 29, 'telefone': '1234-3333'}